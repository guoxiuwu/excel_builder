package com.dslengine.excel.builder.exception

class DataSourceRegistrationException extends RuntimeException {

    public DataSourceRegistrationException() {
        super()
    }

    public DataSourceRegistrationException(Throwable cause) {
        super(cause)
    }

    public DataSourceRegistrationException(String message) {
        super(message)
    }


    public DataSourceRegistrationException(String message, Throwable cause) {
        super(message, cause)
    }

}
